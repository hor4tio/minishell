#include "libft.h"

char			*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*dst;
	size_t	i;

	i = 0;
	if (s == NULL)
		return (NULL);
	if (len > ft_strlen(s) - start)
		len = ft_strlen(s) - start;
	if (ft_strlen(s) <= start)
	{
		if (!(dst = (char *)malloc(1)))
			return (NULL);
		dst[i] = '\0';
		return (dst);
	}
	if (!(dst = (char *)malloc(len * sizeof(char) + 1)))
		return (NULL);
	while (i < len)
	{
		dst[i] = s[start];
		i++;
		start++;
	}
	dst[i] = '\0';
	return (dst);
}
